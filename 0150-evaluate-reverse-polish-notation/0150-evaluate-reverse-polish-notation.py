class Solution:
    def evalRPN(self, tokens: List[str]) -> int:
        stack = []
        for char in tokens:
            if char in '+-*/':
                n2 = stack.pop()
                n1 = stack.pop()
                
                if char == '+':
                    stack.append(n1 + n2)
                if char == '-':
                    stack.append(n1 - n2)
                if char == '*':
                    stack.append(n1 * n2)
                if char == '/':
                    if abs(n1) < abs(n2):
                        stack.append(0)
                    else:
                        stack.append(int(n1 / n2))
                
            if char.strip('-').isnumeric():
                stack.append(int(char))
           
        return stack[0]
    
